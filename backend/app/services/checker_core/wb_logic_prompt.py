"""
Central WB Card Logic prompt blocks.
Keeps one source of truth for AI behavior across audit/fix/title/description flows.
"""

from __future__ import annotations


WB_CARD_LOGIC_CORE = """
WB CARD LOGIC SPEC (обязательные правила):
1) Работай только с подтвержденными фактами из карточки/характеристик/фото-контекста.
2) Не придумывай значения, если они не подтверждены данными.
3) Для характеристик с allowed_values выбирай строго из allowed_values.
4) Поля дат/сертификатов/деклараций НЕ анализируй и НЕ исправляй.
5) AI предлагает вариант, но backend отдельно решает severity и requires_human_check.
6) Если уверенности недостаточно для безопасного подтверждения, верни requires_human_check=true вместо догадки.
7) exact_fix возвращай только если значение подтверждено данными; иначе используй suggestion_kind=candidate или draft_text.
""".strip()


WB_TITLE_SPEC = """
TITLE SPEC:
- Формула: Категория + ключевой признак + (конструктив) + (назначение) + (цвет при необходимости).
- Длина: 40-60 символов (идеально 35-50, но не выходить за системный лимит).
- Обязательно: категория и подтвержденный ключевой признак.
- Цвет добавлять только когда он подтвержден и не создает конфликтов.
- Запрещено: бренд, пол (женский/мужской/детский), маркетинг, эмоции, CAPS, эмодзи, спецсимволы, повторы.
- Запрещено: шаблон "для + существительное" (используй прилагательные).
""".strip()


WB_DESCRIPTION_SPEC = """
DESCRIPTION SPEC:
- Длина: 1000-1800 символов.
- Формат: 3-6 абзацев, каждый абзац 2-4 предложения.
- Структура: вступление -> конструкция/посадка -> материал (только подтвержденный) -> назначение -> особенности/уход.
- Только нейтральные фактические формулировки.
- Запрещено: маркетинг, эмоции, обещания эффекта, ссылки, телефоны, CAPS, эмодзи, списки.
- Должна быть согласованность с title и характеристиками.
""".strip()


WB_ATTRIBUTES_SPEC = """
ATTRIBUTES SPEC:
- Значения должны соответствовать WB allowed values.
- Учитывай min/max ограничения по количеству значений.
- Удаляй/очищай признаки, не относящиеся к товару.
- Не допускай взаимоисключающих или фантазийных значений.
- Для color-полей возвращай parent color (backend сам расширяет палитру).
""".strip()


WB_OUTPUT_SPEC = """
OUTPUT RULES:
- Возвращай только JSON в требуемом формате.
- Не возвращай советы вместо значения.
- Для каждого issue — одно конкретное исправление.
""".strip()


def build_wb_logic_block(*, include_output: bool = True) -> str:
    blocks = [
        WB_CARD_LOGIC_CORE,
        WB_TITLE_SPEC,
        WB_DESCRIPTION_SPEC,
        WB_ATTRIBUTES_SPEC,
    ]
    if include_output:
        blocks.append(WB_OUTPUT_SPEC)
    return "\n\n".join(blocks)
