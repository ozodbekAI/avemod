def _payload_decimal0(payload: dict | None, key: str) -> Decimal:
    if not isinstance(payload, dict):
        return Decimal("0")
    try:
        return Decimal(str(payload.get(key) or 0))
    except (InvalidOperation, TypeError, ValueError):
        return Decimal("0")


def issue_is_operational_only_non_final(
    code: str | None,
    payload: dict | None = None,
) -> bool:
    normalized_code = str(code or "").strip().lower()
    if normalized_code == "order_without_sale_or_return":
        return True
    if normalized_code != "finance_reconciliation_mismatch":
        return False
    revenue_delta = abs(_payload_decimal0(payload, "revenueDelta"))
    for_pay_delta = abs(_payload_decimal0(payload, "forPayDelta"))
    return revenue_delta <= Decimal("1") and for_pay_delta > Decimal("1")


