    @classmethod
    def _is_supply_source_unmatched(cls, issue, payload: dict | None = None) -> bool:
        normalized_payload = payload if payload is not None else cls._payload(issue)
        if str(getattr(issue, "code", None) or "").lower() != "unmatched_sku":
            return False
        source_kind = str(normalized_payload.get("sourceKind") or "").strip().lower()
        source_domains = {
            str(item).strip().lower()
            for item in (normalized_payload.get("sourceDomains") or [])
            if str(item).strip()
        }
        classification_reason = (
            str(normalized_payload.get("classificationReason") or "").strip().lower()
        )
        return (
            source_kind == "source_level"
            and source_domains == {"supplies"}
            and classification_reason in {"missing_nm_id", "source_level_missing_nm_id"}
        )

